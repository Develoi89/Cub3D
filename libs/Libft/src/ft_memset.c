#include "../include/libft.h"

void	*ft_memset(void *b, int c, size_t n)
{
	size_t	i;
	char	*d;

	i = 0;
	d = (char *)b;
	while (i < n)
	{
		*d = c;
		d++;
		i++;
	}
	return (b);
}
